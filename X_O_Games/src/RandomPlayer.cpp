// Set player symbol and name as Random Computer Player
RandomPlayer::RandomPlayer (char symbol, int first_dimension, int second_dimension):Player(symbol)
{
    this->first_dimension = first_dimension;
    this->second_dimension = second_dimension;
    this->name = "Random Computer Player";
    cout << "My names is " << name << endl;
}

// Generate a random move
void RandomPlayer::get_move (int& x, int& y) {
    x = (int) (rand()/(RAND_MAX + 1.0) * first_dimension);
    y = (int) (rand()/(RAND_MAX + 1.0) * second_dimension);
}
